//===========================================================================
// Function:   _satf
//===========================================================================
//
// C Usage:    extern float _satf(float A, float Pos, float Neg);
//
//---------------------------------------------------------------------------
// Algorithm:  Saturates the value "A" between the values "Pos" and "Neg".
//			
//---------------------------------------------------------------------------

float _satf(float A, float Pos, float Neg)
{
     if( A > Pos )
         A = Pos;
     if( A < Neg )
         A = Neg;
     
     return A;
}                          
							
